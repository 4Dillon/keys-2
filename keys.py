import math

# Функция для безопасного ввода положительного целого числа от пользователя
# Принимает строку-приглашение (prompt) и возвращает целое число
# Если пользователь вводит некорректные данные, функция повторяет запрос

def get_positive_integer(prompt="Введите положительное целое число: "):
    while True:
        user_input = input(prompt)  # Запрашиваем ввод
        try:
            value = int(user_input)  # Пробуем преобразовать ввод в целое число
            if value < 0:
                print("Ошибка: число должно быть неотрицательным.")  # Проверяем, что число неотрицательное
            else:
                return value  # Если все хорошо, возвращаем значение
        except ValueError:
            print("Ошибка: введите целое число.")  # Если ввод не число, выводим ошибку

# Основная функция программы
# Получает число от пользователя, вычисляет факториал и выводит результат

def main():
    n = get_positive_integer()  # Получаем положительное целое число
    try:
        result = math.factorial(n)  # Вычисляем факториал с помощью math.factorial (оптимизировано для больших чисел)
        print(f"Факториал числа {n} равен {result}")  # Выводим результат
    except (OverflowError, ValueError) as e:
        # Обрабатываем возможные ошибки при вычислении факториала
        print(f"Ошибка при вычислении факториала: {e}")

# Запуск программы, если файл запускается как основной
if __name__ == "__main__":
    main()
